import React from 'react';
import { Meteor } from 'meteor/meteor';
import { render } from 'react-dom';
/*import '../../../../../stylesheets/tab.css'*/

import MlTabComponent from '../../../../../commons/components/tabcomponent/MlTabComponent'

export default class MlIdeatorPortfolioAbout extends React.Component{
  constructor(props){
    super(props)
    this.state =  {tabs: []};
  }

  getTabComponents(){

    let tabs = [
      {component:<div tabTitle="First tab" key="1"> First </div>},
      {component:<div tabTitle="second tab" key="2"> second </div>},
      {component:<div tabTitle="third tab" key="3"> third </div>}
    ]

    return tabs;
  }

  componentWillMount(){
    let tabs = this.getTabComponents();
    let components = [];
    for(var i = 0; i < tabs.length; i++){
      components.push(tabs[i].component)
    }
    this.setState({tabs:components ||[]});
  }

  render(){
    let tabs = this.state.tabs;
    return <MlTabComponent tabs={tabs}/>
  }
}
